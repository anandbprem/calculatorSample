public class Calculator {
    public Calculator(){

    }

    public long sumar(long a, long b){
        return a + b;
    }

    public long restar(long a, long b){
        return a - b;
    }

    public long multiplicar(long a, long b){
        return a * b;
    }

    public long dividir(long a, long b){
        return a / b;
    }
}
